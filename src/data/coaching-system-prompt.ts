/**
 * Get a contextualized prompt for a specific diagnosis code
 */
export function getContextualizedPrompt(diagnosisCode: string): string {
  const [rawTypeCode, rawLevel] = diagnosisCode.toUpperCase().split('-');
  const typeContext = PRIVATE_TYPE_CONTEXT[rawTypeCode];
  const levelContext = PRIVATE_LEVEL_CONTEXT[rawLevel];
  const privateContext = [typeContext, levelContext].filter(Boolean);

  return `${coachingSystemPrompt}

---

## クライアントに関する非表示の参考情報

${privateContext.length > 0 ? privateContext.map((context) => `- ${context}`).join('\n') : '- 診断情報は利用せず、ユーザーの言葉を中心に対応する。'}

これは言葉選びを調整するためだけの内部文脈です。この見立ての存在や分類をユーザーへ説明せず、ユーザーの言葉と感情を中心に返答してください。

---

${coachingConversationPriorityPrompt}
`;
}
